import React, { useState, createRef } from "react";
import {
  StyleSheet,
  TextInput,
  View,
  Text,
  ScrollView,
  Image,
  Keyboard,
  TouchableOpacity,
  KeyboardAvoidingView
} from "react-native";
import { API_ROOT } from "../constants";
import AsyncStorage from "@react-native-async-storage/async-storage";
import Loader from "./Components/Loader";

/****************************************************/
